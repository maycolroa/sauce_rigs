<template>
  <div>
    <header-module
      title="MATRIZ DE PELIGROS"
      subtitle="ADMINISTRAR MATRICES"
      url="industrialsecure-dangermatrix-menu"
    />

    <div class="col-md">
      <b-card no-body>
        <b-card-header class="with-elements">
          <div class="card-title-elements">
            <b-btn style="margin-top: 10px;" v-if="auth.can['dangerMatrix_c']" :to="{name:'industrialsecure-dangermatrix-create'}" variant="primary">Crear Matriz de Peligros</b-btn>
            <b-btn style="margin-top: 10px;" v-if="auth.can['dangerMatrix_view_report']" :to="{name:'industrialsecure-dangermatrix-report'}" variant="primary">Reporte</b-btn>
            <b-btn style="margin-top: 10px;" v-if="auth.can['dangerMatrix_c']" :to="{name:'industrialsecure-dangermatrix-tags'}" variant="primary">Administrar Tags</b-btn>
            <b-btn style="margin-top: 10px;" variant="primary" :to="{name:'industrialsecure-dangermatrix-addfields'}" v-b-tooltip.top title="Configurar Campos Adicionales"><i class="ion ion-md-document"></i></b-btn>
            <b-btn style="margin-top: 10px;" v-if="auth.can['dangerMatrix_c']" variant="primary" href="/templates/dangermatriximport" target="blank" v-b-tooltip.top title="Generar Plantilla"><i class="fas fa-file-alt"></i></b-btn>
            <b-btn style="margin-top: 10px;" v-if="auth.can['dangerMatrix_c']" variant="primary" @click="importMessage()" v-b-tooltip.top title="Importar"><i class="fas fa-upload"></i></b-btn>
            <input id="fileInputImport" type="file" style="display:none" v-on:input="importDangerMatrix"/>
            <b-btn style="margin-top: 10px;" v-if="auth.can['dangerMatrix_c']" :to="{name:'industrialsecure-dangermatrix-log-qualification'}" variant="primary" v-b-tooltip.top title="Log Calificaciones"><i class="oi oi-list"></i></b-btn>
            <b-btn style="margin-top: 10px;" v-if="auth.can['dangerMatrix_c']" :to="{name:'industrialsecure-dangermatrix-export-masive'}" variant="primary" v-b-tooltip.top title="Exportacion Masiva"><i class="fas fa-download"></i></b-btn>
            <b-btn style="margin-top: 10px;" v-if="auth.can['dangerMatrix_c']" variant="primary" href="/templates/dangermatriximportMassive" target="blank" v-b-tooltip.top title="Generar Plantilla para importaci贸n masiva"><i class="fas fa-file-alt"></i></b-btn>
            <b-btn style="margin-top: 10px;" v-if="auth.can['dangerMatrix_c']" variant="primary" @click="importMessage('massive')" v-b-tooltip.top title="Importar Peligros Masivos"><i class="fas fa-upload"></i></b-btn>
            <input id="fileInputImportMassive" type="file" style="display:none" v-on:input="importDangerMatrix"/>
          </div>
        </b-card-header>
        <b-card-body>
             <vue-table
                configName="industrialsecure-dangermatrix"
                :customColumnsName="true" 
                v-if="auth.can['dangerMatrix_r']" 
                ></vue-table>
        </b-card-body>
    </b-card>

     <!-- modal confirmation for import -->
    <b-modal ref="modalConfirmationImport" class="modal-slide" hide-header hide-footer>
      <p class="text-justific mb-4">
        Estimado Usuario para realizar la importaci贸n el archivo debe cumplir lo siguiente:<br><br>

        <ol>
          <li>Formato excel (*.xlsx).</li>
          <li>Incluir las cabeceras de los campos en la primera fila del documento.</li>
          <li>Solo se leera la primera hoja del documento (En caso de tener mas de una).</li>
        </ol>

      </p>
      <b-btn block variant="primary" @click="importConfirmation()">Aceptar</b-btn>
      <b-btn block variant="default" @click="toggleModalConfirmationImport(false)">Cancelar</b-btn>
    </b-modal>
    </div>
  </div>
</template>

<script>
import Alerts from '@/utils/Alerts.js';

export default {
  name: 'dangermatrix',
  metaInfo: {
    title: 'Matriz de Peligros'
  },  
  data () {
    return {
      type: ''
    }
  },
  methods: {
    importDangerMatrix(e){
      var formData = new FormData();
      var imagefile = e.target.files;

      if (this.type == 'massive')      
        var urlImport = '/industrialSecurity/dangersMatrix/importMassive';
      else
        var urlImport = '/industrialSecurity/dangersMatrix/import';      

      formData.append("file", imagefile[0]);
      axios.post(urlImport, formData, {
          headers: {
            'Content-Type': 'multipart/form-data'
          }
      })
      .then(response => {        
        document.getElementById('fileInputImport').value = ''
        document.getElementById('fileInputImportMassive').value = ''
        Alerts.warning('Informaci贸n', 'Se inicio la importaci贸n, se le notificara a su correo electronico cuando finalice el proceso.');
      }).catch(error => {        
        document.getElementById('fileInputImport').value = ''
        document.getElementById('fileInputImportMassive').value = ''
        Alerts.error('Error', 'Se ha generado un error en el proceso, por favor contacte con el administrador');
      });    
    },
    importMessage(massive) {
      this.toggleModalConfirmationImport(true)

      if (massive && massive == 'massive')
        this.type = 'massive';
    },
    importConfirmation() {
      this.toggleModalConfirmationImport(false);
      if (this.type == 'massive')
        document.getElementById('fileInputImportMassive').click()
      else
        document.getElementById('fileInputImport').click()

    },
    toggleModalConfirmationImport(toggle) {
      if (toggle)
        this.$refs.modalConfirmationImport.show()
      else
        this.$refs.modalConfirmationImport.hide();
    }
  },
}
</script>
